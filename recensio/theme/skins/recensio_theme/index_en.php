			<h1>online from the end of 2010!</h1>
			<p>recensio.net will create a Europe-wide, multi-language platform for reviews of historical literature.<br /><br />
			recensio.net is committed to the principles of Open Access.</p> 	
		    </div>
		    <div class="box">
			<h2>international & multi-periodical</h2>
			<p>We focus on work published in Europe and featuring European topics.<br />
			Our navigational languages are English, German and French, while the reviews may be written in any European language.</p>	
		    </div>
		</div>
		<div id="column2">
		    <div class="box">
			<h2>recensio.net brings together “classic” reviews</h2>
			<p>Editorial offices of journals who have only published in print so far will be able to publish their review sections prominently online (pre- or post-print).<br /><br /></p>	
		    </div>
		    <div class="box">
			<h2>recensio.net en route to web 2.0</h2>
			<p>Authors will be able to present the core statements of their publications (monographs or articles) on recensio.net.<br />
			Gradually, moderated user comments will create “live reviews” based on these presentations.</p>	
		    </div>
