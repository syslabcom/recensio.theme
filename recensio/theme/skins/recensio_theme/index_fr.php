			<h1>Mise en ligne à la fin 2010 !</h1>
			<p>recensio.net est conçu comme une plateforme plurilingue d’envergure européenne destinée à héberger des recensions d’ouvrages d’histoire scientifiques.<br /><br />
			recensio.net suit le principe du libre accès.</p> 	
		    </div>
		    <div class="box">
			<h2>internationale & transpériode</h2>
			<p>Les comptes-rendus critiques publiés sur recensio.net se concentreront sur les ouvrages publiés en Europe portant sur un ou des pays d’Europe. Les langues de navigation sur la plateforme seront l’anglais, l’allemand et le français. Les recensions pourront être rédigées dans toute langue européenne.</p>	
		    </div>
		</div>
		<div id="column2">
		    <div class="box">
			<h2>recensio.net rassemble des comptes-rendus critiques « classiques »</h2>
			<p>Les rédactions de revues, qui publient habituellement leurs recensions sur un support papier, pourront éditer avec une grande visibilité leurs rubriques de recensions en ligne sur recensio.net (en prépublication ou en postdocument).<br /><br /></p>	
		    </div>
		    <div class="box">
			<h2>recensio.net en route<br />vers le « web 2.0 »</h2>
			<p>Les auteurs auront la possibilité de publier sur recensio.net les principales thèses développées dans leurs travaux (articles ou monographies). Des commentaires d’internautes filtrés par un modérateur donneront progressivement lieu à des « recensions animées et interactives ».</p>	
		    </div>
